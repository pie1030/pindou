export interface BeadColor {
  code: string;
  series: string;
  hex: string;
  r: number;
  g: number;
  b: number;
  L: number;
  a: number;
  B: number;
}

function srgbToLinear(c: number) {
  const s = c / 255;
  return s > 0.04045 ? Math.pow((s + 0.055) / 1.055, 2.4) : s / 12.92;
}

function labF(t: number) {
  return t > 216 / 24389 ? Math.cbrt(t) : (24389 / 27 * t + 16) / 116;
}

function computeLab(r: number, g: number, b: number): [number, number, number] {
  const rl = srgbToLinear(r), gl = srgbToLinear(g), bl = srgbToLinear(b);
  const x = labF((rl * 0.4124564 + gl * 0.3575761 + bl * 0.1804375) / 0.95047);
  const y = labF(rl * 0.2126729 + gl * 0.7151522 + bl * 0.0721750);
  const z = labF((rl * 0.0193339 + gl * 0.1191920 + bl * 0.9503041) / 1.08883);
  return [116 * y - 16, 500 * (x - y), 200 * (y - z)];
}

function bc(code: string, r: number, g: number, b: number): BeadColor {
  const hex = '#' + [r, g, b].map(v => v.toString(16).padStart(2, '0')).join('').toUpperCase();
  const series = code.replace(/\d+$/, '');
  const [L, a, B] = computeLab(r, g, b);
  return { code, series, hex, r, g, b, L, a, B };
}

